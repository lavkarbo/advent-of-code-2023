def move_rock(
    origin: int, destination: int, column: int, rock_map: dict[tuple[int, int], str]
) -> None:
    rock_map[(origin, column)] = ""
    rock_map[(destination, column)] = "O"
    print(f"Moving rock from {(origin, column)} to {(destination, column)}")


