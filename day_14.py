def count_rock_weight(rock_map):
    n = len(rock_map)
    count = 0
    for i, row in enumerate(["".join(r) for r in rock_map]):
        n_rocks = len(re.findall(r"O", row))
        count += n_rocks * (n - i)
        # print(f"{n_rocks} rocks at {n-i}: {n_rocks*(n-i)}")
    return count


def tilt_platform(rock_map):
    for i in range(len(rock_map)):
        for j, char in enumerate(rock_map[i]):
            if char == "O":
                for r in range(i - 1, -1, -1):
                    # print(f"Rock at {i, j}: Checking at {r, j}")
                    if rock_map[r][j] == ".":
                        if r == 0:
                            move_rock_2(i, r, j, rock_map)
                            break
                        else:
                            continue
                    elif rock_map[r][j] != "." and r < i - 1:
                        move_rock_2(i, r + 1, j, rock_map)
                        break
                    else:
                        break


